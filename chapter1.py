#Python 출력 -> 한줄 주석
""" -> 여러줄 주석
프로그래밍은 자료와 처리라는 두가지 표현으로 말할 수 있어요

(예시) 수강생들이 제출하는 과제는 자료, 제출 여부를 확인하는 과정을 처리라고 합니다.

[자료형] : 문자, 숫자, 불(참과 거짓 -> Ture or False)
"123", 123 : 동적할당

String(문자형) : str -> 외워야 함
- " " -> "안녕하세요", "123", '123'
- " I'm "

Number(숫자형) : int
-1234, 445, 3423

Boolean(불) : 참과 거짓 (True, False -> 1, 0)

함수 -> 만들고 싶은 기능을 정의해서언제든 불러서 쓸 수 있게 만든 것
괄호를 열고 닫는 형태 -> 함수를 구분할 수 있는 특징중에 하나
separator
"""

